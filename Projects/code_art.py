# ###############################################
# ### SETUP ###
import turtle
# ###############################################

t = turtle.Turtle()
t.penup()
t.goto(-100, -100)
t.color("purple")
t.pendown()

for i in range (4):
    t.forward(100)
    t.left(40)



# ###############################################
# ### ENDING ###
turtle.exitonclick()
